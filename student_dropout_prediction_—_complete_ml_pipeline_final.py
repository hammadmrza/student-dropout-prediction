def compute_fairness(y_true, y_pred, mask_a, mask_b, label_a, label_b, dropout_class=0):
    """Compute fairness metrics between two groups."""
    # Demographic parity
    dp_a = (y_pred[mask_a] == dropout_class).mean()
    dp_b = (y_pred[mask_b] == dropout_class).mean()

    # Disparate impact ratio
    di = min(dp_a, dp_b) / max(dp_a, dp_b) if max(dp_a, dp_b) > 0 else 1.0

    # True positive rates (dropout recall)
    actual_a = (y_true[mask_a] == dropout_class)
    actual_b = (y_true[mask_b] == dropout_class)
    tpr_a = ((y_pred[mask_a] == dropout_class) & actual_a).sum() / actual_a.sum() if actual_a.sum() > 0 else 0
    tpr_b = ((y_pred[mask_b] == dropout_class) & actual_b).sum() / actual_b.sum() if actual_b.sum() > 0 else 0

    # False positive rates
    not_a = (y_true[mask_a] != dropout_class)
    not_b = (y_true[mask_b] != dropout_class)
    fpr_a = ((y_pred[mask_a] == dropout_class) & not_a).sum() / not_a.sum() if not_a.sum() > 0 else 0
    fpr_b = ((y_pred[mask_b] == dropout_class) & not_b).sum() / not_b.sum() if not_b.sum() > 0 else 0

    return {
        'Group A': label_a, 'Group B': label_b,
        'n_A': mask_a.sum(), 'n_B': mask_b.sum(),
        'DI Ratio': di, 'DI Flag': 'FAIL' if di < 0.8 else 'PASS',
        'TPR_A': tpr_a, 'TPR_B': tpr_b, 'TPR_diff': abs(tpr_a - tpr_b),
        'FPR_A': fpr_a, 'FPR_B': fpr_b, 'FPR_diff': abs(fpr_a - fpr_b),
    }

